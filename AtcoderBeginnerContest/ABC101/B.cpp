int sum_digit(int n)
{
    int res = 0;
    while (n)
    {
        res += n % 10;
        n /= 10;
    }
    return res;
}

int main()
{
    int n;
    cin >> n;

    cout << ((n % sum_digit(n) == 0) ? "Yes" : "No") << '\n';
    return (0);
}
